from enum import Enum

class SpanningCharacter(Enum):
    DASH = "-"
    EQUALS = "="
    UNDERSCORE = "_"
    LINE = "─"
    DOUBLE_LINE = "═"
    BLOCK_0 = "░"
    BLOCK_1 = "▒"
    BLOCK_2 = "▓"
    BLOCK_3 = "█"
    BLOCK_LOWER = "▄"
    BLOCK_UPPER = "▀"
    BLANK = " "
    STAR = "*"
    TILDE = "~"

